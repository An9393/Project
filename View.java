    public void loadData(ArrayList<Model> list) {
        DefaultTableModel tableModel = (DefaultTableModel) TableSanPham.getModel();
        tableModel.setRowCount(0);
        for (Model model : list) {
            tableModel.addRow(new Object[]{
                model.getMaSanPham(), model.getTenSanPham(), model.getTrangThai(), model.getNgayTao(), model.getIdDanhMuc()
            });
        }
    }

