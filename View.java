    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String trangThai = "";
        if (cboxActive.isSelected()) {
            trangThai = "Active";
        }
        else {
            trangThai = "Inactive";
        }
        Model newModel = new Model();
        newModel.setMaSanPham(ma);
        newModel.setTenSanPham(ten);
        newModel.setTrangThai(trangThai);
        newModel.setNgayTao(new Date());
        Boolean kq = listing.addNew(newModel);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Nhập dữ liệu thành công");
            loadData(listing.getList());
        }
        else {
            JOptionPane.showMessageDialog(this, "Nhập dữ liệu không thành công");
        }
    }//GEN-LAST:event_jButton1MouseClicked

